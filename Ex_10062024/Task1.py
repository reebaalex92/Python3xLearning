# Explain the difference between the = operator and the == operator in Python.

# Assignment operator (=) is used to assign a value to a variable.
# Example:
x = 5
name = "John"
print(x)
print(name)

# Equality operator (==) is used to compare two values for equality.
# Equality returns boolean values.
# Example:
num1 = 10
num2 = 10
print(num1 == num2)
a = 20
b = 30
print(a == b)

# ** operator do in Python, and its use
# ** is a power operator that raises the first operand to the power of the second operand.
# Used as a number raise to the power
# Example:
u = 10
v = 2
print(u ** v)  # Output: 100

# the ^ operator do in Python, and in what context is it commonly used
# The ^ operator is a bitwise XOR operator in Python.
# It performs a bitwise exclusive OR operation on the corresponding bits of the two operands.
# Used in cryptography, bit manipulation,error correction
# Example:
x = 5  # Binary representation of 5 is 0101
y = 3  # Binary representation of 3 is 0011
# The result of x^y is 0110 which is binary representation of 6.
print(x ^ y)  # Output: 6






