# Literals
# Variable_name = Variable_value
# var_name -> Identifier
# var_value -> Literal
# literals are the values which are assigned to the variables
# Literals are numeric or non-numeric
a = 10
b = 10.5
c = "Hello"
d = True
e = []
f = {}
g = None
print(a, b, c, d, e, f, g)
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))
