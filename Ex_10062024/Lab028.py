# Operators
# 1. Assignment Operators (=)
name = "John"
# 2. Compare Operators (==) Always returns boolean value
# In python 1 represents True and 0 represents False
v1 = (1 == True)
print(v1)
v2 = (0 == False)
print(v2)
# 3. Unary Operators
a = 10
print(a)
b = -5
print(b)
c = a+b
print(c)

# 4. Not Operator (only works for boolean values)
is_male = False
print(not is_male)
# 5. is  operator -> returns boolean value
d = 10
e = 11
print(d is e)
my_list = [1, 2, 3]
my_list2 = [1, 2, 3]
print(my_list is my_list2)
# is  operator is used to compare the memory address of the two variables
