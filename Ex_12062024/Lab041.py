# Program to find the maximum of two numbers
a = int(input("Enter the first number\n"))
b = int(input("Enter the second number\n"))
result = max(a, b)
print(result)

# Same program using if else
a = int(input("Enter the first number\n"))
b = int(input("Enter the second number\n"))
if a > b:
    print(a)
else:
    print(b)