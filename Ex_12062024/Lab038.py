# Conditions and Loops
# This code demonstrates the use of conditional statements and loops in Python

# if-else  statement
# Syntax: if condition:
#              code to be executed if condition is true
#          else:
#              code to be executed if condition is false


# Take a input as user age and check if the user is eligible to go to club.
age = int(input("Enter your age: "))
if age > 18:
    print(" Go to the club.")
else:
    print("Not allowed")
