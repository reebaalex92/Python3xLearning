# Rules to follow creating variables
# 1. No spaces in variable names
# 2. No numbers in the start of variable names
# 3. Can't use any special characters in variable names
# 4. Variable names are case sensitive
# 5. Can't use reserved keywords in variable names

# Variable names are case sensitive
name = "Reeba"
print(type(name))
# 123 = 234 # This will give error
# Variable names start from a-z, A-Z, 0-9, _
# _ is used
_name = "Reeba"
first_name = "Reeba"
# Python loves snake case which means all the words are separated by underscore
first_name_of_user = "Reeba92"
