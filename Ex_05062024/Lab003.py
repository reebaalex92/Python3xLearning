# Studying about print command
print("Hello", "World", "123", "3.14")
# using seperator -
print("Hello", "World", "123", "3.14", sep='-')
# using seperator #
print("Hello", "World", "123", "3.14", sep='#')
# Using double space "  "
print("Hello", "World", "123", "3.14", sep='  ')
# using no seperator
print("Hello", "World", "123", "3.14", sep="")
# having only one argument.Argument defined by ("") not by (,)
print("Hi, My name is Reeba")
# having two arguments
print("Hi, My name is Reeba", "Alexander")
# using seperator -
print("Hi, My name is Reeba", "Alexander", sep='-')

