# By default take new line at the end
print("Hi I am Reeba")
print("I am a manual tester")
# By adding end = '-'
print("Hi I am Reeba", end="-")
print("I am a manual tester")
# By adding end and seperator
print("Hi", "Iam", "Reeba", sep="-", end="\t")
print("I am a manual tester")
# By adding nothing in end
print("Hi I am Reeba", end="")
print("I am a manual tester")