fruit1 = ('Apple', 'Orange', 'Banana')
fruit2 = ('Watermelon', 'Grape', 'Mango')
new_fruit = (fruit1, fruit2)
print(new_fruit)  # (('Apple', 'Orange', 'Banana'), ('Watermelon', 'Grape', 'Mango'))
print(new_fruit[0])  # ('Apple', 'Orange', 'Banana')
print(new_fruit[1])  # ('Watermelon', 'Grape', 'Mango')
print(new_fruit[0][0])  # Apple
print(new_fruit[0][1])  # Orange
print(new_fruit[0][2])  # Banana
print(new_fruit[1][0])  # Watermelon
print(new_fruit[1][1])  # Grape
print(new_fruit[1][2])  # Mango
